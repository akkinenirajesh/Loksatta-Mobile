package org.loksatta.android.core;

public class Leader extends AbstractCore {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * Name of the Leader
	 */
	private String name;

	/**
	 * Position of the Leader
	 */
	private String position;

	/**
	 * Email id of the Leader
	 */
	private String email;

	/**
	 * Phone Number of the Leader
	 */
	private String phone;

	/**
	 * Facebook Id
	 */
	private String fbId;

	/**
	 * Google Plus Id
	 */
	private String gId;

	/**
	 * Id of the Twitter
	 */
	private String tId;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the position
	 */
	public String getPosition() {
		return position;
	}

	/**
	 * @param position
	 *            the position to set
	 */
	public void setPosition(String position) {
		this.position = position;
	}

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email
	 *            the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}

	/**
	 * @param phone
	 *            the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}

	/**
	 * @return the fbId
	 */
	public String getFbId() {
		return fbId;
	}

	/**
	 * @param fbId
	 *            the fbId to set
	 */
	public void setFbId(String fbId) {
		this.fbId = fbId;
	}

	/**
	 * @return the gId
	 */
	public String getgId() {
		return gId;
	}

	/**
	 * @param gId
	 *            the gId to set
	 */
	public void setgId(String gId) {
		this.gId = gId;
	}

	/**
	 * @return the tId
	 */
	public String gettId() {
		return tId;
	}

	/**
	 * @param tId
	 *            the tId to set
	 */
	public void settId(String tId) {
		this.tId = tId;
	}

}
